package Online_Shop;

public class Customer {
    private String name;
    private String email;

    //          Getter and Setter

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }
}
